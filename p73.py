#aggregation with tuple(len/min/max/sum)
t=(10,20,30,40)
print(len(t))
print(min(t))
print(max(t))
print(sum(t))

#searching in tuple
print(10 in t)
print(50 in t)

#Sorting in tuple
print(sorted(t))    #asc order
print(sorted(t,reverse=True))   #desc order

#concatenation in tuple
a=(1,2,3,4)
b=(4,5,6,7,8)
c=a+b
print(c)

#Repeatation in tuple
a=(20,40,60,80)
b=2*2
print(b)#or
print(2*a)



















