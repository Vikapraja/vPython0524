#Assignment Operators
    #Simple Assignment
        #var=value/var/expression

x=5
y=x
z=x+y
print(x,y,z)

    #Multiple Assignment
        #var1,var2,...var-n=value-1,value-2,.....value-n

a,b,c=5,10,12
print(a,b,c)

    #Chained Assignment
        #var-1=var-2=......=value

a=b=c=10
print(a,b,c)

    #Compound Assignment

a=5
a*=3  #a=a*3
print(a)

a+=2
print(a)

a-=5
print(a)


