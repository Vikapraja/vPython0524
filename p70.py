#list methods
x=[10,20,5]
print(x)

#append(value):add given value to last of list
x.append(2)
print(x)

#inser(index,value):
x.insert(0,50)
print(x)

#remove(value):removes only first occurance of given value
x.remove(20)
print(x)

#pop(index):removes value from given index
x.pop(1)
print(x)

#pop():removes value from last of list
x.pop()
print(x)
